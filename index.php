            $(document).ready(function () {
                $("#nextPage").click(function (event) {
                        getPagination($("#page").html());
                });
            });
            function getPagination(pg) {
                if ($("#trigger").html() === "true") {
                    $("#trigger").html("false");
                    $("#res").fadeTo("slow", 0.3);
                    setTimeout(function () {
                        $.ajax({
                            type: "POST",
                            url: "actions.php",
                            data: {
                                exec: 'scroll',
                                pg: pg
                            },
                            dataType: 'json',
                            processData: true,
                            success: function (data) {
                                if (data.results !== "") {
                                	console.log(data);
                                    $("#res").append(data.results);
                                    setTimeout(function () {
                                        if (data.totalItens < 6) {
                                            $("#trigger").html("false");
                                            $("#nextPage").fadeOut();
                                        }
                                        else {
                                            $("#trigger").html("true");
                                        }
                                        $("#page").html(data.pg);
                                    }, 500);
                                }
                                else {
                                    $("#res").append('<div class="col-md-12"><h3>Sem Mais Resultados...</h3></div>');
                                }
                                $("#res").fadeTo("slow", 1);
                            }
                        });
                    }, 1000);
                }
            }
            getPagination(1);
        </script>
	
